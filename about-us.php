        var btn = document.getElementById('aboutToggleBtn');
        if (content.style.display === 'none') {
            content.style.display = 'block';
            btn.innerHTML = 'View Less <i class="fas fa-chevron-up" style="font-size:0.8rem;"></i>';
        } else {
            content.style.display = 'none';
            btn.innerHTML = 'View More <i class="fas fa-chevron-down" style="font-size:0.8rem;"></i>';
        }
    }
