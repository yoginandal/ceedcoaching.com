                if (currentAmount < finalAmount) {
                    currentAmount += increment;
                    if (currentAmount > finalAmount) {
                        currentAmount = finalAmount;
                    }
                    prizeAmount.textContent = formatCurrency(Math.floor(currentAmount));
                    requestAnimationFrame(animateCounter);
