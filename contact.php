    <script>
    document.getElementById('contactForm').addEventListener('submit', function(e) {
        e.preventDefault();

        var form = this;
        var phone = form.querySelector('#phone').value.trim();
        var msgDiv = document.getElementById('formMessage');

        if (phone && !/^[6-9]\d{9}$/.test(phone)) {
            msgDiv.style.display = 'block';
            msgDiv.style.background = '#f8d7da';
            msgDiv.style.color = '#721c24';
            msgDiv.textContent = 'Please enter a valid 10-digit phone number starting with 6, 7, 8, or 9.';
            return;
        }

        var btn = form.querySelector('button[type="submit"]');
        var originalText = btn.textContent;
        btn.disabled = true;
        btn.textContent = 'Sending...';
        msgDiv.style.display = 'none';

        var formData = new FormData(form);

        // Collect UTM parameters
        var urlParams = new URLSearchParams(window.location.search);
        formData.append('utm_source', urlParams.get('utm_source') || '');
        formData.append('utm_medium', urlParams.get('utm_medium') || '');
        formData.append('utm_campaign', urlParams.get('utm_campaign') || '');

        var scriptUrl = 'https://script.google.com/macros/s/AKfycby675rWvo1ZL0yXGzmxPGFybci2ZXEhQLXrHc0DthsLajvC4qt2DOnUI7vvd0KhTvJ8/exec';

        fetch(scriptUrl + '?' + new URLSearchParams(formData).toString())
            .then(function(response) { return response.text(); })
            .then(function(data) {
                msgDiv.style.display = 'block';
                if (data === 'Success') {
                    msgDiv.style.background = '#d4edda';
                    msgDiv.style.color = '#155724';
                    msgDiv.textContent = 'Thank you! Your message has been sent successfully.';
                    form.reset();
                } else {
                    msgDiv.style.background = '#d4edda';
                    msgDiv.style.color = '#155724';
                    msgDiv.textContent = 'Thank you! Your message has been sent successfully.';
                    form.reset();
                }
            })
            .catch(function(error) {
                msgDiv.style.display = 'block';
                msgDiv.style.background = '#f8d7da';
                msgDiv.style.color = '#721c24';
                msgDiv.textContent = 'Network error. Please check your connection and try again.';
            })
            .finally(function() {
                btn.disabled = false;
                btn.textContent = originalText;
            });
    });
    </script>
